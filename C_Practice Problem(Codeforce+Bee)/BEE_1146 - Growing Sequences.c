#include<stdio.h>
int main()
{
    int a=1,i;
    while(1) {
        scanf("%d",&a);
        if(a!=0) {
            for(i=1; i<=a; i++) {
                if(i==a) {
                    printf("%d",i);
                }
                else {
                    printf("%d ",i);
                }
            }
            printf("\n");
        }
        else {
            break;
        }
    }
    return 0;
}
