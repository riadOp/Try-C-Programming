#include<stdio.h>

int main()
{
    int x,y;
    int i,j=0;
    
    scanf("%d%d",&x,&y);
    if(x>y){
    for(i=x-1;i>y;i--){
    if(i%2!=0)
    j=j+i;
    }
    printf("%d\n",j);
    }
    else if(x<y){
    for(i=x+1;i<y;i++){
    if(i%2!=0)
    j=j+i;
    }
    printf("%d\n",j);
    }
    return 0;
}
