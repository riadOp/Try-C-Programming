#include<stdio.h>
int main()
{
    long long int a,b,i,j,fact1=1,fact2=1;
    while((scanf("%lld%lld",&a,&b))!=EOF){
    fact1=1;
    fact2=1;
    for(i=1;i<=a;i++){
        fact1=fact1*i;
    }
    for(j=1;j<=b;j++){
        fact2=fact2*j;
    }
    printf("%lld\n",fact1+fact2);
    }
    return 0;
}
