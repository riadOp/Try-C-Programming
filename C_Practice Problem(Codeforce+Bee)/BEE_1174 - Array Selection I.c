#include<stdio.h>

int main()
{
    int i,j;
    float a[100],b;
    for(i=0; i<100; i++) {
        scanf("%f",&b);
        if(b<11.0) {
            a[i]=b;
        }
        else {
            a[i]=20;
        }
    }
    for(j=0; j<100; j++) {
        if(a[j]!=20) {
            printf("A[%d] = %0.1f\n",j,a[j]);
        }
    }
    return 0;
}
