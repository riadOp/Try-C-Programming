#include<stdio.h>
int main()
{
    int i,t,f;
    char n[5];
    scanf("%d",&t);
    for(i=0;i<t;i++){
        scanf("%s%d",&n,&f);
        if(n[0]=='T'&&n[1]=='h'&&n[2]=='o'&&n[3]=='r'&&n[4]=='\0'){
            printf("Y\n");
        }
        else{
            printf("N\n");
        }
    }
    return 0;
}
