#include<stdio.h>
int main()
{
    int a,b,i,j,k=0;
    scanf("%d",&b);
    for(i=0;i<b;i++){
        scanf("%d",&a);
        for(j=2;j<=a/2;j++){
            if(a%j==0){
                k=1;
            }
        }
        if(k==0){
            printf("%d eh primo\n",a);
        }
        else {
            printf("%d nao eh primo\n",a);
        }
        k=0;
    }
    return 0;
}
